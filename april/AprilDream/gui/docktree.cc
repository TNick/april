/* ------------------------------------------------------------------------- */
DockTree::DockTree	( MW * mw )
	: Dock( mw, "World tree" ),
	d_( NULL )
{
	APRDBG_CDTOR;
	/* stub */
}
/* ========================================================================= */

/* ------------------------------------------------------------------------- */
DockTree::~DockTree	( void )
{
	APRDBG_CDTOR;
	if ( d_ != NULL )
	{
		deconstruct();
	}
}
/* ========================================================================= */

/* ------------------------------------------------------------------------- */
void				DockTree::construct			( void )
{
	d_ = new DockTree_p();
	d_->tv_ = new QTreeWidget( dock() );
	dock()->setWidget( d_->tv_ );
	
	d_->tv_->setHeaderHidden( true );
}
/* ========================================================================= */

/* ------------------------------------------------------------------------- */
void				DockTree::deconstruct			( void )
{
	d_->tv_->deleteLater();
	delete d_;
	d_ = NULL;
}
