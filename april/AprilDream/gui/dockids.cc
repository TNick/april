/* ------------------------------------------------------------------------- */
DockIds::DockIds	( MW * mw )
	: Dock( mw, "Defined identificators" ),
	d_( NULL )
{
	APRDBG_CDTOR;
	/* stub */
}
/* ========================================================================= */

/* ------------------------------------------------------------------------- */
DockIds::~DockIds	( void )
{
	APRDBG_CDTOR;
	if ( d_ != NULL )
	{
		deconstruct();
	}
}
/* ========================================================================= */

/* ------------------------------------------------------------------------- */
void				DockIds::construct			( void )
{
	d_ = new DockIds_p();
	d_->tv_ = new QTreeWidget( dock() );
	dock()->setWidget( d_->tv_ );
	
	QStringList sl;
	sl.append( "Value" );
	sl.append( "Description" );
	d_->tv_->setHeaderLabels( sl );
	
}
/* ========================================================================= */

/* ------------------------------------------------------------------------- */
void				DockIds::deconstruct			( void )
{
	d_->tv_->deleteLater();
	delete d_;
	d_ = NULL;
}
