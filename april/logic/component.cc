/* ------------------------------------------------------------------------- */
bool				Component::save				( QSettings & stg ) const
{ FUNC_ENTRY;
	bool b = true;
	stg.beginGroup( "april-Component" );
	
	for (;;)	{
		stg.setValue( "payload_", payload_.i_ );
		break;
	}
	stg.endGroup();
	
	return b;
}
/* ========================================================================= */

/* ------------------------------------------------------------------------- */
bool				Component::load				( QSettings & stg )
{ FUNC_ENTRY;
	bool b = true;
	stg.beginGroup( "april-Component" );
	
	for (;;)	{
		payload_.i_ = stg.value( "payload_", 0 ).toULongLong( &b );
		
		break;
	}
	
	stg.endGroup();
	
	return b;
}
