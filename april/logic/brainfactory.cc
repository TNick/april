/* ------------------------------------------------------------------------- */
Brain *				BrainFactory::create			( Actor * ag, ID id )
{
	Q_UNUSED( id );
	if ( world() == NULL )
		return NULL;
	
	Brain * ret = new Brain( ag, 1, 10 );
	return ret;
}
