/* ------------------------------------------------------------------------- */
Actuator *			ActuatorFactory::create				( Actor * ag, ID id )
{
	Q_UNUSED( id );
	if ( world() == NULL )
		return NULL;
	
	Actuator * ret = new Actuator( ag, 1, 10 );
	return ret;
}
